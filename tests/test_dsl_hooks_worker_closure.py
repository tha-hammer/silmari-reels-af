def test_compile_error_never_reaches_stitch_or_finish(a1_refs, tmp_path, monkeypatch):
    """B5. See the module docstring note on monkeypatching CT-1's span."""
    calls: list[str] = []
    monkeypatch.setattr(app_mod, "stitch_footage_reel",
                        lambda *a, **k: calls.append("stitch"))
    monkeypatch.setattr(app_mod, "finish_reel", lambda *a, **k: calls.append("finish"))

    bogus = tmp_path / "bogus.ts.md"
    bogus.write_text("00:00:04.120  this text appears nowhere in the source transcript\n")

    result = asyncio.run(
        dsl_hooks_to_reels(
            **{**a1_refs, "composite_ref": str(bogus)},
            out_dir=str(tmp_path / "work"),
            fetch_segment=lambda req: pytest.fail("must not fetch"),
            uploader=lambda *a, **k: pytest.fail("must not upload"),
        )
    )

    assert result["error"] == "dsl_compile_failed"
    assert result["diagnostics"]
    assert calls == []  # the load-bearing assertion


