def _fetch(source: Path):
    return lambda req: source


class _FakeTextProvider:
    """Stands in for the LLM (hook text). The render mechanics stay REAL.

    Matches the precedent set by tests/test_finish_closure.py: "Only whisper, the
    LLM and image generation are faked — the render mechanics are all real." The
    LLM is an external service at the edge of the span, not a step of it —
    finish_reel itself really runs and really burns pixels.
    """

    def ai(self, system=None, user=None, schema=None, **kw):
        if schema is not None:
            return {"hook": "THEY DON'T REASON", "moments": []}
        return "THEY DON'T REASON"


class _FakeImageProvider:
    """Stands in for image GENERATION only — returns a real PNG.

    Same boundary as the LLM: image generation is an external service at the edge
    of the span. The generated bytes are real, so the ffmpeg overlay burn that
    consumes them is real too.

    NOTE: passing image_provider=None would NOT disable images — it means "use the
    production default", and OpenRouterProvider constructs fine without a key and
    only fails lazily at generate_image. An explicit fake is required.
    """

    async def generate_image(self, *, prompt=None, model=None, n=1, **kw):
        import base64
        import io

        from PIL import Image

        buf = io.BytesIO()
        Image.new("RGB", (1024, 1024), (200, 30, 30)).save(buf, format="PNG")
        b64 = base64.b64encode(buf.getvalue()).decode()

        class _Img:
            b64_json = b64
            url = None

        class _Resp:
            images = [_Img()]

        return _Resp()


def _uploader_ok(local_path, *, run_id, filename=None, **kw):
    return f"https://bucket.example.com/outputs/{run_id}/{filename or 'reel.mp4'}"


def _uploader_fixed(local_path, *, run_id, filename=None, **kw):
    """Deterministic delivery URL for the golden-parity test.

    The real uploader embeds run_id (which is random per run), so a live URL
    cannot be pinned in a fixture. _normalize must NOT strip download_url — it is
    the contract field B17 exists to pin — so the URL is made stable here instead.
    """
    return "https://bucket.example.com/outputs/GOLDEN/reel.mp4"


def _uploader_none(local_path, *, run_id, filename=None, **kw):
    """The real fail-soft behavior of storage.upload_reel when the bucket is unset."""
    return None


def _normalize(result: dict) -> dict:
    """Strip only non-deterministic fields. NEVER strips download_url/error — those
    are the contract B17 pins."""
    out = dict(result)
    for key in ("run_id", "timings_s", "video_path", "reels"):
        out.pop(key, None)
    return out


