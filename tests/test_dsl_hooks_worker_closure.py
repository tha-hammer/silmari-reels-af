# ── B17 (BLOCKING): CT-1 <-> CT-2 golden parity ────────────────────


def test_ct2_golden_fixture_matches_real_worker_output(a1_refs, lavfi_source, tmp_path, request):
    """PARITY: the CP body CT-2 replays must equal what the worker ACTUALLY returns.

    REAL_WORKER_RESULT is a LIVE INVOCATION, never a hand-authored constant — a
    constant here would make this a tautology and silently void the CT-1/CT-2
    closure guarantee (R5).

    Regenerate (never hand-edit the fixture):
        uv run --extra dev python -m pytest \\
          tests/test_dsl_hooks_worker_closure.py --regenerate-golden -q
    """
    _require_ffmpeg()

    real_worker_result = asyncio.run(
        dsl_hooks_to_reels(
            **a1_refs,
            out_dir=str(tmp_path / "work"),
            fetch_segment=_fetch(lavfi_source),
            text_provider=_FakeTextProvider(),
            image_provider=_FakeImageProvider(),
            uploader=_uploader_fixed,
        )
    )

    if request.config.getoption("--regenerate-golden"):
        GOLDEN.parent.mkdir(parents=True, exist_ok=True)
        GOLDEN.write_text(
            json.dumps(
                {"status": "succeeded", "result": _normalize(real_worker_result)},
                indent=2, sort_keys=True,
            )
            + "\n",
            encoding="utf-8",
        )

    assert GOLDEN.exists(), (
        f"golden fixture missing: {GOLDEN}. Regenerate with --regenerate-golden"
    )
    golden = json.loads(GOLDEN.read_text(encoding="utf-8"))

    assert golden["result"] == _normalize(real_worker_result)
    # The contract fields must survive normalization.
    assert "download_url" in golden["result"]
    assert golden["status"] == "succeeded"
